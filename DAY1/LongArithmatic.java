package DAY1;

public class LongArithmatic {
public static void main(String[] args) {
	long a = 2500l;
	long b = 2300l;
	double c = 280.0;
	double d = 20.0;
	System.out.println((a+b)+  " addition");
	System.out.println((a-b)+ " difference");
	System.out.println((a*b)+  " multipication");
	System.out.println((a/b)+  " division");
	System.out.println((a%b)+  " module");
	System.out.println((c+d)+ " addition");
	System.out.println((c-d)+  " difference");
	System.out.println((c*d)+  " multipication");
	System.out.println((c/d)+ " division");
	System.out.println((c%d)+  " module");

	
}
}
